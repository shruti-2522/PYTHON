list1 = ['shruti', 'niki', 'hitesh', 'Rushali', 'pradhunya', 'vishakha', 22, 20, 2]
print(list1)

num = [1, 2, 3, 4, 5, 6]
print(num)

# length,maximum,minimum:
print(len(list1))
print(max(num))
print(min(num))

# Append,Insert,Remove,pop:
num.append(10)
print(num)

num.insert(2, 12)
print(num)

num.remove(2)
print(num)

num.pop()
print(num)

# Slicing
print(num[::-1])
